let rating = 2;

switch (rating) {
    case 1:
        console.log("Poor Performance");
        break;

    case 2:
        console.log("Needs Improvement");
        break;

    case 3:
        console.log("Met Expectation");
        break;

    case 4:
        console.log("Exceed Expectation");
        break;

    case 5:
        console.log("OutStanding Performance");
        break;

    default:
        console.log("Talk to HR");
        break;
}